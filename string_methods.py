name = "Avinash Mankar"
# len() method
# It returns the lenth of the string including white spaces
length = len(name)
print(length)

#lower() method
# It converts all the string in lower case
print(name.lower())

#upper() method
# It converts all the string in upper case
print(name.upper())

#title() function
# It converts first letter into capital and otthers in lower case
print(name.title())

#count() method
# This method counts the specific character in the string
print(name.count("a"))