# range function
'''
Syntax :
         range(start,stop,stepwise)  # stop is always stop-1
'''
a = range(10)
print(a[0])
print(a[1])
print(a[2])
print(a[3])
print(a[4])
print("*************************")
b = range(1,10)
print(b[0])
print(b[1])
print(b[2])
print(b[3])
print(b[4])
print("*************************")
c = range(1,10,2)
print(c[0])
print(c[1])
print(c[2])
print(c[3])
print(c[4])
print("*************************")
d= range(-1,-10,-2)
print(d[0])
print(d[1])
print(d[2])
print(d[3])
print(d[4])

